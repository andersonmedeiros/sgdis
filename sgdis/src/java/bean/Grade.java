package bean;

/**
 *
 * @author andersondepaula
 */
public class Grade {
    String grade, fase, disciplina, assunto;
    int chdiurna, chnoturna;

    public String getGrade() {
        return grade;
    }

    public void setGrade(String grade) {
        this.grade = grade;
    }

    public String getFase() {
        return fase;
    }

    public void setFase(String fase) {
        this.fase = fase;
    }

    public String getDisciplina() {
        return disciplina;
    }

    public void setDisciplina(String disciplina) {
        this.disciplina = disciplina;
    }

    public String getAssunto() {
        return assunto;
    }

    public void setAssunto(String assunto) {
        this.assunto = assunto;
    }

    public int getChdiurna() {
        return chdiurna;
    }

    public void setChdiurna(int chdiurna) {
        this.chdiurna = chdiurna;
    }

    public int getChnoturna() {
        return chnoturna;
    }

    public void setChnoturna(int chnoturna) {
        this.chnoturna = chnoturna;
    }
    
    
}
